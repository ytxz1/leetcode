/*
 * LeetCode 2657. 找出前缀公共数组
 *
 * 题目描述：
 *   给你两个下标从 0 开始长度为 n 的整数排列 A 和 B。
 *   A 和 B 的 前缀公共数组 定义为数组 C，其中 C[i] 是数组 A 和 B
 *   到下标为 i 之前公共元素的数目。
 *   请你返回 A 和 B 的 前缀公共数组。
 *
 *   注意：长度为 n 的排列包含 1 到 n 的元素恰好一次。
 *
 * ============================================================
 * 解题思路：
 * ============================================================
 *
 * 1. 问题本质：
 *    对于每个位置 i，需要统计 A[0..i] 和 B[0..i] 中
 *    有多少个元素同时出现在两个前缀中。
 *
 * 2. 算法选择：计数数组
 *    - 用一个 count 数组记录每个元素「出现的次数」
 *    - 每次处理 A[i] 和 B[i] 时，将对应计数 +1
 *    - 如果某个元素的计数变为 2，说明它同时出现在 A 和 B 的前缀中
 *    - 维护一个 common 变量，记录当前共有元素的数量
 *
 * 3. 关键点：
 *    - 因为 A 和 B 都是 1~n 的排列，每个值只会出现一次
 *    - 所以 count 最多为 2（A 中出现一次 + B 中出现一次）
 *    - 当 count 从 1 变为 2 时，common++；这就是新发现的公共元素
 *
 * 4. 复杂度分析：
 *    - 时间复杂度：O(n)，遍历一次数组
 *    - 空间复杂度：O(n)，count 数组占 O(n)
 */

#include <vector>

using namespace std;

class Solution {
public:
    vector<int> findThePrefixCommonArray(vector<int>& A, vector<int>& B) {
        int n = A.size();

        // 结果数组 C
        vector<int> C(n);

        // 计数数组：记录每个元素出现的次数
        // 索引对应元素值（1~n），值为出现次数（0, 1, 2）
        // 使用 n+1 大小因为元素范围是 1~n
        vector<int> count(n + 1, 0);

        // common：当前前缀中公共元素的数量
        int common = 0;

        /*
         * 遍历每个位置 i
         *
         * 对应代码第 55~72 行
         */
        for (int i = 0; i < n; i++) {
            /*
             * 处理 A[i]：将 A[i] 的计数 +1
             *
             * 如果 A[i] 之前已经在 B 中出现过（count == 1），
             * 那么现在 count 变为 2，说明 A[i] 是公共元素
             */
            count[A[i]]++;
            if (count[A[i]] == 2) {
                common++;
            }

            /*
             * 处理 B[i]：将 B[i] 的计数 +1
             *
             * 同理，如果 B[i] 之前已经在 A 中出现过（count == 1），
             * 那么现在 count 变为 2，说明 B[i] 是公共元素
             */
            count[B[i]]++;
            if (count[B[i]] == 2) {
                common++;
            }

            /*
             * 记录当前位置的公共元素数量
             *
             * 注意：A[i] 和 B[i] 可能是同一个值（如果 A[i] == B[i]），
             * 此时该值的 count 会从 0 直接变为 2，
             * 两次 if 判断各触发一次，common 增加 2，
             * 但实际上只多了一个公共元素。
             *
             * 但题目说明 A 和 B 都是排列，每个值在各自数组中只出现一次，
             * 所以 A[i] == B[i] 时，count 从 0→1（第一次 if）再 1→2（第二次 if），
             * common 增加 2 是正确的——因为这个值确实在两个数组中都出现了。
             *
             * 举例：A = [1,2,3], B = [1,2,3]
             *   i=0: A[0]=1, count[1]=0→1（不触发）, B[0]=1, count[1]=1→2（触发）, common=1
             *   结果正确：前缀 [1] 和 [1] 有 1 个公共元素
             */
            C[i] = common;
        }

        return C;
    }
};
